export const getAllTokens = () => {
  tokens.forEach((token) => {
    let icon = null;
    try {
      icon = require(`../assets/icons/${token.tokenSymbol}.png`).default;
    } catch (e) {
      try {
        icon = require(`../assets/icons/${token.tokenSymbol}.svg`).default;
      } catch (e1) {
        try {
          icon = require(`../assets/icons/${token.tokenSymbol}.webp`).default;
        } catch (e2) {
          try {
            icon =
              require(`cryptocurrency-icons/svg/color/${token.tokenSymbol}.svg`).default;
          } catch (e3) {
            icon =
              require(`cryptocurrency-icons/svg/color/generic.svg`).default;
          }
        }
      }
    }

    if (!token.tokenIcon) token.tokenIcon = icon;
  });

  return tokens;
};
